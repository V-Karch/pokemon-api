pub async fn version_info() -> WebResult<impl warp::Reply> {
    let response_json = &model::VersionInfoResponse {
        status: "Success".to_string(),
        version: "pokemon-api v0.0.1".to_string(),
    };
    Ok(json(response_json))
}

pub async fn list_unsupported() -> WebResult<impl warp::Reply> {
    let response_json = json(&UNSUPPORTED_NAMES.to_vec());
    Ok(warp::reply::with_status(response_json, StatusCode::OK))
}